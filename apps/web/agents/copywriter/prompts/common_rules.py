"""
common_rules.py
콘텐츠 타입 공통 집필 규칙.

모든 콘텐츠 타입(추천형, 리뷰형, 정보형)에 공통으로 적용되는 집필 규칙.
해요체 사용, Bold 금지, 불릿 금지 등 핵심 스타일 가이드.

이 규칙은 content_prompts 테이블의 common_rules 레코드와 동기화되며,
DB에 저장된 값이 있으면 DB 값을 우선 사용한다.
"""


# ──────────────────────────────────────────────────────
# 공통 집필 규칙 상수
# ──────────────────────────────────────────────────────

COMMON_WRITING_RULES = """### 공통 집필 규칙 (모든 콘텐츠 타입 필수 적용)

**1. 문체 — 해요체 필수**
- 모든 문장은 반드시 해요체(~해요, ~이에요, ~거든요, ~인데요, ~했어요)로 작성한다.
- "~이다", "~한다", "~된다" 등 학술적/딱딱한 문체는 절대 사용하지 않는다.
- 독자에게 말하듯 친근하고 자연스러운 톤을 유지한다.

**2. Bold(굵은 글씨) 절대 금지**
- 본문에서 **Bold** 서식을 절대 사용하지 않는다.
- 강조가 필요하면 이모지(✅, 💡, ⭐ 등)나 문장 구조로 자연스럽게 표현한다.

**3. 불릿(-, *, •) 리스트 사용 금지**
- 마크다운 불릿 리스트(-, *, •)를 사용하지 않는다.
- 나열이 필요하면 이모지 리스트 또는 번호 리스트를 사용한다.
  예시)
  ✅ 첫 번째 항목이에요
  ✅ 두 번째 항목이에요
  또는
  1️⃣ 첫 번째 항목이에요
  2️⃣ 두 번째 항목이에요

**4. 이모지 활용 (적극 사용)**
- 소제목, 리스트, 포인트 강조에 이모지를 적극 사용한다.
- 소제목 예시: "## 🏕️ 합천 캠핑장 추천 TOP 5"
- 자주 사용할 이모지: ✅ ⭐ 💡 📌 🔥 👉 💰 📍 🎯 ❗ 👍 🏷️

**5. 이미지 묘사 가이드 필수**
- 각 섹션에 [이미지 가이드: 상세 촬영 방향/구도/분위기 1~2문장] 삽입.
- 최소 6개 이상의 이미지 가이드 포함.
- 예시: [이미지 가이드: 캠핑장 전경을 드론으로 촬영한 항공뷰, 초록빛 잔디와 텐트들이 보이는 풍경]

**6. 경험/후기형 서술 — 체험 톤 필수**
- 모든 콘텐츠를 실제 방문/사용한 것처럼 1인칭 경험 톤으로 작성한다.
- "직접 가봤는데요", "실제로 써봤어요", "솔직히 말하면요" 등 체험형 표현 사용.
- #내돈내산, #솔직후기 스타일 유지.

**7. 학술적/AI 티 나는 표현 금지**
- "종합적으로 분석하면", "따라서", "결론적으로", "이상으로" 등 학술 표현 금지.
- "AI 요약 답변", "본 글에서는" 등 AI가 쓴 것 같은 표현 금지.
- "~할 수 있다", "~것으로 판단된다", "~로 보인다" 등 불확실 표현 금지.
- 대신 "~거든요", "~더라고요", "~인 것 같아요"처럼 구어체 사용.

**8. H2/H3 소제목 규칙**
- H2는 반드시 이모지 + 제목 형태: "## 🏕️ 캠핑장 시설 비교"
- H3는 이모지 선택적: "### 텐트 사이트" 또는 "### ⛺ 텐트 사이트"
- H2 최소 4개 이상, H3는 자연스럽게 배치.

**9. 상단 핵심 요약 (AI 요약 대체)**
- "## AI 요약 답변" 대신 자연스러운 도입부를 작성한다.
- 제목 아래 첫 2~3문장에서 핵심 내용을 요약하되, 경험형 해요체로 작성한다.
- 예시: "캠핑 좋아하는 분들이라면 합천 캠핑장 한번쯤 고민해보셨을 거예요. 제가 직접 다녀온 곳 중 가장 좋았던 5곳을 골라봤어요!"

**10. FAQ 작성 규칙**
- FAQ 질문은 실제 검색 쿼리 형태 + 해요체 ("~인가요?", "~할 수 있나요?")
- 답변도 해요체, 단정적이되 친근한 톤 ("네, ~이에요!", "~거든요!")
- 최소 5개 이상.
"""

# ──────────────────────────────────────────────────────
# 콘텐츠 타입 목록
# ──────────────────────────────────────────────────────

CONTENT_TYPES = {
    "list": {
        "name": "추천형 (리스트)",
        "description": "TOP N 추천, 비교, 순위형 콘텐츠",
        "icon": "📋",
    },
    "review": {
        "name": "리뷰형 (후기)",
        "description": "실제 체험/방문 후기, 시간순 경험 서술",
        "icon": "✍️",
    },
    "info": {
        "name": "정보형 (가이드)",
        "description": "체크리스트/요약표 중심 정보 제공",
        "icon": "📖",
    },
}


def get_common_rules() -> str:
    """공통 집필 규칙 문자열 반환."""
    return COMMON_WRITING_RULES


def get_content_type_info(content_type: str) -> dict:
    """콘텐츠 타입 정보 반환."""
    return CONTENT_TYPES.get(content_type, CONTENT_TYPES["list"])
